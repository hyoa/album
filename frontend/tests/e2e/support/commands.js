Cypress.Commands.add('login', (email, password) => {
  const apiUri = Cypress.env('VUE_APP_API_URI')

  return cy
    .request('POST', `${apiUri}/user/login`, { email: 'jmatsounga@gmail.com', password: '###' })
    .then(({ body: { token } }) => {
      window.localStorage.setItem('album-token', token)
    })
})
